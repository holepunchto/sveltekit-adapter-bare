// Platform-specific asset reader. Resolved via the `imports` map in
// ./package.json: desktop → lib/read-asset.js (file:// via bare-fs),
// Android → lib/read-asset.android.js (bundle protocol).
import { read_asset } from '#read-asset'

// bare-fetch's Body mixin doesn't implement formData(); SvelteKit form actions
// call request.formData() on POSTs, so polyfill it here. Supports the two
// content-types browsers actually send from <form>: urlencoded and multipart.
if (typeof Request.prototype.formData !== 'function') {
  Request.prototype.formData = async function formData() {
    const contentType = this.headers.get('content-type') || ''
    const fd = new FormData()

    if (contentType.startsWith('application/x-www-form-urlencoded')) {
      // The urlencoded spec says `+` decodes to space, but whichever global
      // URLSearchParams bare exposes doesn't honor that. Normalize `+` →
      // `%20` first so decoding is correct regardless of implementation.
      const body = (await this.text()).replace(/\+/g, '%20')
      const params = new URLSearchParams(body)
      for (const [k, v] of params) fd.append(k, v)
      return fd
    }

    if (contentType.startsWith('multipart/form-data')) {
      const m = contentType.match(/boundary=(?:"([^"]+)"|([^;]+))/)
      if (!m) throw new TypeError('Missing multipart boundary')
      parse_multipart(await this.text(), m[1] || m[2], fd)
      return fd
    }

    throw new TypeError(`Unsupported content-type for formData(): ${contentType}`)
  }
}

/**
 * Minimal multipart/form-data parser. Handles text fields only — file uploads
 * would need binary-safe chunking which isn't needed for this adapter's
 * current use cases.
 * @param {string} body
 * @param {string} boundary
 * @param {FormData} fd
 */
function parse_multipart(body, boundary, fd) {
  const delim = '--' + boundary
  const parts = body.split(delim)
  // Skip the preamble (parts[0]) and the trailing closing boundary (parts[last]).
  for (let i = 1; i < parts.length - 1; i++) {
    let part = parts[i]
    if (part.startsWith('\r\n')) part = part.slice(2)
    if (part.endsWith('\r\n')) part = part.slice(0, -2)
    const sep = part.indexOf('\r\n\r\n')
    if (sep < 0) continue
    const headers = part.slice(0, sep)
    const value = part.slice(sep + 4)
    const name = headers.match(/Content-Disposition:[^\r\n]*name="([^"]+)"/i)
    if (name) fd.append(name[1], value)
  }
}
